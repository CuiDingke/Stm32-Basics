int main(void)
{
   Key_GPIO_Config();

    while (1)
    {
			if(Key_Scan(GPIOA,GPIO_Pin_0)== KEY_ON) 
			{
			sum=300;
			tim3_Init(sum);
			}
			
			if(Key_Scan(GPIOA,GPIO_Pin_1)== KEY_ON) 
			{
			sum=500;
			tim3_Init(sum);
			}
			
			if(Key_Scan(GPIOD,GPIO_Pin_2)== KEY_ON) 
			{
			sum=800;
			tim3_Init(sum);
			}
    }
}
